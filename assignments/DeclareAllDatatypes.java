package Assignments_week1;

public class DeclareAllDatatypes {

	public static void main(String[] args) {
		//Declare and initialize the Variables for all the Datatypes
		byte x = 50;
		System.out.println("byte datatype - "+ x);
		short y = 60;
		System.out.println("short datatype - "+ y);
		int z = 2025;
		System.out.println("int datatype - "+ z);
		long l = 78675364576878l;
		System.out.println("long datatype - "+ l);
		float f = 7.14f;
		System.out.println("float datatype - "+ f);
		double d = 2.54367;
		System.out.println("double datatype - "+ d);
		char sym = 'C';
		System.out.println("char datatype - "+ sym);
		boolean data = true;
		System.out.println("boolean datatype - "+ data);
		String name = "Chaithanya M";
		System.out.println("String datatype - "+ name);
	}

}
